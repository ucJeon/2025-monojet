def parse_updates(text: str) -> dict[str, tuple[float, float]]:
    """
    C++ initializer 형태:
      { "Signal_...", 1.23e-3, 4.56e-6 },
    를 파싱해서 {sample: (xsec, err)} dict로 반환.
    // 로 시작하는 주석 라인은 무시.
    같은 sample이 여러 번 나오면 마지막 값이 최종값.
    """
    out = {}

    # // 주석 라인 제거
    lines = []
    for ln in text.splitlines():
        ln_strip = ln.strip()
        if ln_strip.startswith("//") or ln_strip == "":
            continue
        # 라인 중간에 // 코멘트가 있으면 그 앞까지만
        ln = ln.split("//", 1)[0]
        lines.append(ln)
    clean = "\n".join(lines)

    # sample, xsec, err 추출
    # sample은 큰따옴표 내부, 숫자는 float(지수표기 포함)
    pat = re.compile(
        r'"(?P<sample>Signal_[^"]+)"\s*,\s*'
        r'(?P<xsec>[+-]?\d+(?:\.\d*)?(?:[eE][+-]?\d+)?)\s*,\s*'
        r'(?P<err>[+-]?\d+(?:\.\d*)?(?:[eE][+-]?\d+)?)'
    )

    for m in pat.finditer(clean):
        s = m.group("sample").strip()
        xsec = float(m.group("xsec"))
        err  = float(m.group("err"))
        out[s] = (xsec, err)

    if not out:
        raise RuntimeError("updates_text에서 어떤 (sample, xsec, err)도 파싱하지 못했습니다. 형식을 확인하세요.")
    return out

def main():
    if not CSV_PATH.exists():
        raise FileNotFoundError(f"CSV not found: {CSV_PATH}")

    updates = parse_updates(updates_text)
    print(f"[INFO] parsed updates: {len(updates)} samples")

    df = pd.read_csv(CSV_PATH)
    df.columns = [c.strip() for c in df.columns]

    # 컬럼명 유효성 체크
    required_cols = ["sample", "xsec [pb]", "xsec_err [pb]"]
    for c in required_cols:
        if c not in df.columns:
            raise RuntimeError(f"CSV column missing: '{c}' (got: {df.columns.tolist()})")

    # 업데이트 전/후 카운트 확인
    df["sample"] = df["sample"].astype(str).str.strip()
    idx = df.set_index("sample")

    missing_in_csv = []
    updated = 0

    for s, (xsec, err) in updates.items():
        if s not in idx.index:
            missing_in_csv.append(s)
            continue
        idx.loc[s, "xsec [pb]"] = xsec
        idx.loc[s, "xsec_err [pb]"] = err
        updated += 1

    # 복원
    df_new = idx.reset_index()

    # 백업 후 덮어쓰기
    bak = CSV_PATH.with_suffix(CSV_PATH.suffix + ".bak")
    if not bak.exists():
        shutil.copy2(CSV_PATH, bak)
        print(f"[INFO] backup created: {bak}")

    df_new.to_csv(CSV_PATH, index=False)
    print(f"[OK] updated rows: {updated}")
    print(f"[OK] overwritten: {CSV_PATH}")

    if missing_in_csv:
        print(f"[WARN] {len(missing_in_csv)} samples were in updates but NOT in CSV.")
        # 너무 길면 상위 일부만
        for s in missing_in_csv[:30]:
            print("  -", s)
        if len(missing_in_csv) > 30:
            print("  ...")

if __name__ == "__main__":
    main()
